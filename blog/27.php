<p>Since the launch of YouTube shorts in 2022, viral shorts have continued to gain momentum. Many content creators on Youtube now use shorts to gain audience and subscribers. However, it has its hitch too just like <a href="https://brightblurb.com/faceless-youtube-automation-channel-ideas/" target="_blank">faceless YouTube channels</a>. Many YouTubers still complain about not getting enough views on shorts.</p>
