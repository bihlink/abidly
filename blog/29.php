<h1>Undoing the Belief in Linear Time: Entering the Holy Instant</h1>
<i>Inspired by the teachings of <b><a href="https://www.reddit.com/r/ACIM/comments/1kbiu6y/undoing_the_belief_in_linear_time_entering_the/">David Hoffmeister</a></b></i>

In a world that measures reality by clocks and calendars, the notion of undoing the belief in linear time may seem radical—even impossible. Yet, spiritual teacher David Hoffmeister, rooted in the non-dual teachings of A Course in Miracles (ACIM), invites us to question this most foundational assumption. What if time, as we perceive it, is not a neutral framework for experience, but a veil that conceals the eternal present—the Holy Instant?
<h3>The Illusion of Linear Time</h3>

Linear time is the belief that life unfolds in a sequence: past, present, and future. This belief system gives rise to identity rooted in memory and expectation. We are taught to define ourselves by what has happened and what may come, but this leaves little room for peace. As Hoffmeister emphasizes, time is not a friend but a device of the ego—used to perpetuate guilt from the past and fear of the future.

ACIM asserts that time itself was made as a defense against the truth. In this view, time is not an objective reality, but a perceptual framework that keeps the mind distracted from its natural state of stillness and unity with God.
<h3>The Holy Instant: A Doorway to Eternity</h3>

Central to Hoffmeister’s message is the transformative power of the Holy Instant—a moment outside of time where forgiveness, healing, and true perception occur. It is not about changing events in time, but about stepping out of time entirely. In the Holy Instant, we remember who we truly are: not bodies with histories, but limitless spirit.

According to Hoffmeister, the Holy Instant is not something to be earned or achieved in the future; it is always available now. It’s a choice—a willingness to let go of judgment, release attachment to outcomes, and accept the eternal presence of Love.
<h3>Forgiveness as the Key</h3>

The path to the Holy Instant is forgiveness—not in the traditional sense of pardoning wrongdoing, but in the radical ACIM sense of recognizing that nothing real has ever been threatened. Forgiveness sees through illusions and restores awareness of divine innocence.

When we forgive, we withdraw our belief in the ego’s narrative, which depends on linear time to survive. We no longer interpret the present through the lens of a painful past. Instead, we allow the present to be what it is: whole, untouched, and deeply healing.
<h3>Living in the Timeless Now</h3>

David Hoffmeister’s teachings are not abstract metaphysics. They are deeply practical. Undoing the belief in linear time means releasing the mind from constant planning, regret, and worry. It means trusting divine guidance in the moment and allowing the Holy Spirit to orchestrate every step.

Living in the Holy Instant does not mean disengaging from life; it means seeing life anew—untainted by the ego’s interpretation. It’s a life of presence, spontaneity, and joy, where miracles become natural.
<h3>Conclusion</h3>

Undoing the belief in linear time is not about escaping the world but seeing it through the eyes of Christ—where all things are made new. David Hoffmeister’s invitation to enter the Holy Instant is a call to awaken from the dream of separation and remember the timeless truth of who we are. The past is over. The future is unnecessary. Only now is real. And in this now, we are already home.
